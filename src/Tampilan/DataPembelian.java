    public void Tampil_ID(){
        try {
            ResultSet rs = stm.executeQuery("SELECT * FROM data_pembelian ORDER BY id_transaksi DESC");

            if (rs.next()) {
                int lastID = Integer.parseInt(rs.getString("id_transaksi").substring(5));
                int newID = lastID + 1;
                String AN = String.format("%05d", newID);
                id_transaksi.setText("PMBLN" + AN);
            } else {
                id_transaksi.setText("PMBLN00001");
            }
        } catch (Exception e) {
            id_transaksi.setText("PMBLN00001");
        }
    }
