    private void btnInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInputActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat fm = new SimpleDateFormat("yyyy-MM-dd");   
        String tgl = String.valueOf(fm.format(tgl_transaksi.getDate()));

        String sql = "insert into data_pembelian values (?,?,?,?,?,?,?,?,?,?,?,?)";
        // INPUT DATA PENJUALAN
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, id_transaksi.getText());
            stat.setString(2, tgl);
            stat.setString(3, id_supplier.getText());
            stat.setString(4, nama_supplier.getText());
            stat.setString(5, alamat.getText());
            stat.setString(6, no_telp.getText());
            stat.setString(7, id_barang.getText());
            stat.setString(8, jenis_baju.getText());
            stat.setString(9, warna.getText());
            stat.setString(10, biaya_persatuan.getText());
            stat.setString(11, jumlah_pesanan.getText());
            stat.setString(12, total_biaya.getText());

            stat.executeUpdate();

            // Update stock barang
            String updateStockQuery = "UPDATE data_barang SET stock = stock + ? WHERE id_barang = ?";
            PreparedStatement updateStockStat = conn.prepareStatement(updateStockQuery);
            updateStockStat.setInt(1, Integer.parseInt(jumlah_pesanan.getText()));
            updateStockStat.setString(2, id_barang.getText());
            updateStockStat.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data akan disimpan dan stok barang diperbarui");
            kosong();
            id_transaksi.requestFocus();
            tabel_pembelian();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Data gagal disimpan. Error: " + e.getMessage());
            tabel_pembelian();
        }
    }//GEN-LAST:event_btnInputActionPerformed

